let day = "Sunday";

switch (day) {
  case "Monday":
    console.log("Javascript");
    break;
  case "Tuesday":
    console.log("Python");
    break;
  case "Wednesday":
    console.log("React");
    break;
  case "Thursday":
    console.log("HTML/CSS");
    break;
  case "Friday":
    console.log("Typescript");
    break;
  case "Saturday":
    console.log("Flutter");
    break;
  case "Sunday":
    console.log("Jest");
    break;
  default:
    console.log("There is no such day.");
}