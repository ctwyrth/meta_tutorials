// e2e (end-to-end)
//   - check that entire end-product performs as expected
//   - slow and time consuming
//   - available frameworks for testing:
//     - WebdriverJS
//     - Protractor
//     - Cypress

// integration
//   - tests how parts work together
//   - faster and cheaper than e2e
//   - available libraries:
//     - React testing library
//     - Enzyme

// unit testing
//   - smallest piece of testable code in app
//   - fast to run, easy to write
//   - available libraries:
//     - Jasmine
//     - Mocha
//     - Karma
//     - qUnit

// JEST
//   - unit testing framework for Javascript
//   - expect( ...action ).toBe( ...result )
//   - Code Coverage: how much of your code is tested
//   - Mocking: separating code from related dependencies
//   - Snapshot testing: no regression in DOM

